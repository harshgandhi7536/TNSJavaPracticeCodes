package day2.conditional;

public class ifElse {

	public static void main(String[] args) {
		int age=18;
		
		if(age >18) {
			System.out.println("You can vote");
		}
		else {
			System.out.println(" You cannot vote");
		}
		
		int x=10;
		int y=20;
		if(x >y)
			System.out.println("X is a greater number");
		else
			System.out.println("Y is greater");
	}

}
